public class postfixincrementexample {

    public static void main(String[] args) {
        // Declare and assign a number to an integer variable
        int number = 5;

        // Print the initial value of the variable
        System.out.println("Initial value: " + number);

        // Use postfix increment operator to increase the value
        number++;

        // Print the value after using the postfix increment operator
        System.out.println("Value after increment: " + number);
    }

}